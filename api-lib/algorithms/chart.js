  plugins: {
    tooltip: {
      enabled: true,
      borderWidth: 0.5,
      borderColor: "black",
      backgroundColor: "rgb(212,212,212,0.6)",
      bodyColor: "black",
      bodyFontStyle: "bold",
      titleFontStyle: "bold",
      titleColor: "black",
      callbacks: {
        label: function (toolTipItems) {
          return (
            "Number of " +
            toolTipItems.dataset.label.toLowerCase() +
            " data is " +
            toolTipItems.raw
          );
        }
      }
    },
    legend: {
      onClick: (e, item, legend) => {
        const index = item.datasetIndex;
        const chart = legend.chart;

        legend.chart.data.datasets.forEach((ds, it) => {
          chart.hide(it);
          ds.hidden = true;
        });

        chart.show(index);
        item.hidden = false;

        chart.update();
      }
    }
  }
